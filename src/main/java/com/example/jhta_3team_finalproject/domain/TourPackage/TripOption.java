package com.example.jhta_3team_finalproject.domain.TourPackage;

public class TripOption {
	String optionId;
	String optionName;
	int optionPrice;
	int optionStock;
	int optionMaxStock;
	String optionDate;
	String cityNo;
	String fileNo;
	String optionMainIMG;

	public String getOptionId() {
		return optionId;
	}

	public void setOptionId(String optionId) {
		this.optionId = optionId;
	}

	public String getOptionName() {
		return optionName;
	}

	public void setOptionName(String optionName) {
		this.optionName = optionName;
	}

	public int getOptionPrice() {
		return optionPrice;
	}

	public void setOptionPrice(int optionPrice) {
		this.optionPrice = optionPrice;
	}

	public int getOptionStock() {
		return optionStock;
	}

	public void setOptionStock(int optionStock) {
		this.optionStock = optionStock;
	}

	public int getOptionMaxStock() {
		return optionMaxStock;
	}

	public void setOptionMaxStock(int optionMaxStock) {
		this.optionMaxStock = optionMaxStock;
	}

	public String getOptionDate() {
		return optionDate;
	}

	public void setOptionDate(String optionDate) {
		this.optionDate = optionDate;
	}

	public String getCityNo() {
		return cityNo;
	}

	public void setCityNo(String cityNo) {
		this.cityNo = cityNo;
	}

	public String getFileNo() {
		return fileNo;
	}

	public void setFileNo(String fileNo) {
		this.fileNo = fileNo;
	}

	public String getOptionMainIMG() {
		return optionMainIMG;
	}

	public void setOptionMainIMG(String optionMainIMG) {
		this.optionMainIMG = optionMainIMG;
	}

}